import Foundation
import UIKit

extension UIColor {
    
    class func oviaGray() -> UIColor {
        return UIColor(red: 153/255, green: 153/255, blue: 153/255, alpha: 1)
    }
    
    class func oviaYellow() -> UIColor {
        return UIColor(red: 255/255, green: 220/255, blue: 57/255, alpha: 1)
    }
}
